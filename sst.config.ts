export default $config({
  app(input) {
    return {
      name: 'person-crud-serverless',
      removal: input?.stage === 'production' ? 'retain' : 'remove',
      protect: ['production'].includes(input?.stage),
      home: 'aws',
    };
  },
  async run() {
    const api = await import('./infra/api');

    return {
      api: api.api.url,
    };
  },
});
